print(f"{x} != {y} 는 {b} 입니다. {type(b)}")
print(f"{x} > {y} 는 {c} 입니다. {type(c)}")
print(f"{x} < {y} 는 {d} 입니다. {type(d)}")
print(f"{x} >= {y} 는 {e} 입니다. {type(e)}")
print(f"{x} <= {y} 는 {f} 입니다. {type(f)}")