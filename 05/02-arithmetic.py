a = 10
b = 3

c = a + b
d = a - b
e = a * b
f = a / b
g = a ** b
h = a // b
i = a % b

print(f"{a} + {b} = {c} type={type(c)}")
print(f"{a} - {b} = {d} type={type(d)}")
print(f"{a} * {b} = {e} type={type(e)}")
print(f"{a} / {b} = {f} type={type(f)}")
print(f"{a} ** {b} = {g} type={type(g)}")
print(f"{a} // {b} = {h} type={type(h)}")
print(f"{a} % {b} = {i} type={type(i)}")